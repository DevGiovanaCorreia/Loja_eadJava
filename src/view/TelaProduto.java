    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
      

    int linha = tabelaProduto.getSelectedRow();

    if (linha == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um produto na tabela!");
        return;
    }

    
    if (txtNome.getText().isEmpty() ||
        txtPreco.getText().isEmpty() ||
        txtEstoque.getText().isEmpty() ||
        cmbCategoria.getSelectedItem() == null) {

        JOptionPane.showMessageDialog(this, "Preencha todos os campos antes de atualizar!");
        return;
    }

    try {
        int id = Integer.parseInt(txtId.getText());
        String nome = txtNome.getText();
        double preco = Double.parseDouble(txtPreco.getText());
        int estoque = Integer.parseInt(txtEstoque.getText());
        Categoria categoria = (Categoria) cmbCategoria.getSelectedItem();
        Fornecedor fornecedor = (Fornecedor) cmbFornecedor.getSelectedItem();

        Produto p = new Produto(id, nome, preco, estoque, categoria,fornecedor);

        produtoDAO.atualizar(p);

        atualizarTabela();
        limparCampos();

        JOptionPane.showMessageDialog(this, "Produto atualizado com sucesso!");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Preço ou estoque inválido!");
    } 
    
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tabelaProduto.getSelectedRow();

    if (linha >= 0) {
        int id = (int) tabelaProduto.getValueAt(linha, 0);

        produtoDAO.remover(id);
        atualizarTabela();
        limparCampos();
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela");
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

