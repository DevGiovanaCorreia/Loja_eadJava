private void atualizarTabela() {

    model.setRowCount(0);

    for (Cliente c : clienteDAO.listar()) {
        model.addRow(new Object[]{
            c.getIdCliente(),
            c.getNomeCliente(),
            c.getCpf(),
            c.getEndereco(),
            c.getTelefone()
        });
    }
}


private void limparCampos() {
    txtNome.setText("");
    txtCpf.setText("");
    txtEndereco.setText("");
    txtTelefone.setText("");
    tabelaCliente.clearSelection();
}


