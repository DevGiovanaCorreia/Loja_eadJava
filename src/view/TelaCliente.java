    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
      int linha = tabelaCliente.getSelectedRow();

    if (linha < 0) {
        JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela");
        return;
    }

    
    if (txtNome.getText().isEmpty() ||
        txtCpf.getText().isEmpty() ||
        txtEndereco.getText().isEmpty() ||
        txtTelefone.getText().isEmpty()) {

        JOptionPane.showMessageDialog(this, "Preencha todos os campos antes de atualizar!");
        return;
    }

    int id = (int) tabelaCliente.getValueAt(linha, 0);

    Cliente cliente = new Cliente(
        id,
        txtNome.getText(),
        txtCpf.getText(),
        txtEndereco.getText(),
        txtTelefone.getText()
    );

    clienteDAO.atualizar(cliente);
    atualizarTabela();
    limparCampos();

    JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso!");
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
          int id = clienteDAO.listar().size() + 1;

    Cliente cliente = new Cliente(
        id,
        txtNome.getText(),
        txtCpf.getText(),
        txtEndereco.getText(),
        txtTelefone.getText()
    );

    clienteDAO.adicionar(cliente);
    atualizarTabela();
    limparCampos();
    
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
         int linha = tabelaCliente.getSelectedRow();

    if (linha >= 0) {
        int id = (int) tabelaCliente.getValueAt(linha, 0);

        clienteDAO.remover(id);
        atualizarTabela();
        limparCampos();
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela");
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

